-- PASSO 7: Conferencia pos-expansao
-- =========================================================
SELECT 'Contagem' AS etapa, COUNT(*) AS total FROM TB_CONTRATO
UNION ALL
SELECT 'Multi-servico restantes', COUNT(*)
FROM TB_CONTRATO WHERE INSTR(SERVICOS_CONTRATADOS, ',') > 0;

SELECT SERVICOS_CONTRATADOS, COUNT(*) AS qtd
FROM TB_CONTRATO
GROUP BY SERVICOS_CONTRATADOS
ORDER BY SERVICOS_CONTRATADOS;
